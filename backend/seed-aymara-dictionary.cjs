// ==================== FUNCIÓN PARA POBLAR LA BASE DE DATOS ====================

async function seedAymaraDictionary() {
  try {
    await client.connect();
    console.log('✅ Conectado a la base de datos SIMIARU');

    // Mapeo de categorías a lección_id
    const categoriaToLeccion = {
      'leccion_25': 25,
      'leccion_26': 26,
      'leccion_27': 27,
      'leccion_28': 28,
      'leccion_29': 29,
      'leccion_30': 30,
      'leccion_31': 31,
      'leccion_32': 32,
      'leccion_33': 33,
      'leccion_34': 34,
      'leccion_35': 35,
      'leccion_36': 36,
      'leccion_37': 37,
      // Categorías adicionales se asignan a lecciones relacionadas
      'naturaleza': 36,
      'cuerpo': 28,
      'tiempo': 25,
      'ubicacion': 27,
      'hogar': 28,
      'cultura': 25,
      'ropa': 33,
      'musica': 32,
      'verbos': 26,
      'caracteristicas': 29
    };

    let totalPalabras = 0;
    let palabrasInsertadas = 0;
    let palabrasDuplicadas = 0;

    console.log('\n📚 Iniciando inserción del diccionario Aymara...\n');

    for (const [categoria, palabras] of Object.entries(vocabularioAymara)) {
      const leccionId = categoriaToLeccion[categoria];
      
      if (!leccionId) {
        console.log(`⚠️  Categoría "${categoria}" sin lección asignada, saltando...`);
        continue;
      }

      console.log(`📖 Procesando: ${categoria} (Lección ${leccionId}) - ${palabras.length} palabras`);

      for (const palabra of palabras) {
        totalPalabras++;
        
        try {
          // Verificar si la palabra ya existe
          const existe = await client.query(
            'SELECT id FROM vocabulario WHERE leccion_id = $1 AND palabra_objetivo = $2',
            [leccionId, palabra.palabra_objetivo]
          );

          if (existe.rows.length === 0) {
            await client.query(`
              INSERT INTO vocabulario (leccion_id, palabra_espanol, palabra_objetivo, categoria, ejemplo_uso, audio_url)
              VALUES ($1, $2, $3, $4, $5, $6)
            `, [
              leccionId,
              palabra.palabra_espanol,
              palabra.palabra_objetivo,
              palabra.categoria,
              palabra.ejemplo_uso || null,
              palabra.audio_url || null
            ]);
            palabrasInsertadas++;
          } else {
            palabrasDuplicadas++;
          }
        } catch (err) {
          console.log(`   ❌ Error con "${palabra.palabra_objetivo}": ${err.message}`);
        }
      }
    }

    console.log('\n==========================================');
    console.log('✅ SEED COMPLETADO');
    console.log('==========================================');
    console.log(`📊 Total palabras procesadas: ${totalPalabras}`);
    console.log(`✅ Palabras insertadas: ${palabrasInsertadas}`);
    console.log(`⏭️  Palabras duplicadas (omitidas): ${palabrasDuplicadas}`);
    console.log('==========================================\n');

  } catch (error) {
    console.error('❌ Error en seed:', error);
  } finally {
    await client.end();
    console.log('🔌 Conexión cerrada');
  }
}

// Ejecutar el seed
seedAymaraDictionary();