async function insertarVocabulario() {
  const client = new Client({
    host: 'localhost',
    port: 5432,
    database: 'simiaru',
    user: 'postgres',
    password: 'simiaru123'
  });

  try {
    await client.connect();
    console.log('Conectado a la base de datos PostgreSQL');

    let totalInsertado = 0;
    let errores = 0;

    // Insertar vocabulario de lecciones específicas (11-23)
    for (let leccionNum = 11; leccionNum <= 23; leccionNum++) {
      const key = `leccion_${leccionNum}`;
      if (vocabulario[key]) {
        console.log(`\nInsertando vocabulario de Lección ${leccionNum}...`);
        for (const palabra of vocabulario[key]) {
          try {
            await client.query(
              `INSERT INTO vocabulario (palabra_objetivo, palabra_espanol, categoria, leccion_id)
               VALUES ($1, $2, $3, $4)
               ON CONFLICT DO NOTHING`,
              [palabra.palabra_objetivo, palabra.palabra_espanol, palabra.categoria, leccionNum]
            );
            totalInsertado++;
          } catch (err) {
            console.error(`Error insertando "${palabra.palabra_objetivo}":`, err.message);
            errores++;
          }
        }
        console.log(`✓ Lección ${leccionNum} completada`);
      }
    }

    // Insertar vocabulario adicional (sin lección específica, asignar a lección base)
    const categoriasAdicionales = [
      { nombre: 'naturaleza', leccionBase: 20 },
      { nombre: 'cielo_clima', leccionBase: 20 },
      { nombre: 'cuerpo_humano', leccionBase: 14 },
      { nombre: 'salud', leccionBase: 14 },
      { nombre: 'hogar', leccionBase: 22 },
      { nombre: 'cultura', leccionBase: 11 },
      { nombre: 'verbos', leccionBase: 11 },
      { nombre: 'tiempo', leccionBase: 16 },
      { nombre: 'ubicacion', leccionBase: 13 }
    ];

    for (const cat of categoriasAdicionales) {
      if (vocabulario[cat.nombre]) {
        console.log(`\nInsertando vocabulario adicional: ${cat.nombre}...`);
        for (const palabra of [cat.nombre]) {
          try {
            await client.query(
              `INSERT INTO vocabulario (palabra_objetivo, palabra_espanol, categoria, leccion_id)
               VALUES ($1, $2, $3, $4)
               ON CONFLICT DO NOTHING`,
              [palabra.palabra_objetivo, palabra.palabra_espanol, palabra.categoria, cat.leccionBase]
            );
            totalInsertado++;
          } catch (err) {
            console.error(`Error insertando "${palabra.palabra_objetivo}":`, err.message);
            errores++;
          }
        }
        console.log(`✓ Categoría ${cat.nombre} completada`);
      }
    }

    console.log('\n========================================');
    console.log('RESUMEN DE INSERCIÓN - DICCIONARIO QUECHUA');
    console.log('========================================');
    console.log(`Total de palabras procesadas: ${totalInsertado}`);
    console.log(`Errores: ${errores}`);
    console.log('========================================');

    // Verificar conteo final
    const result = await client.query(
      'SELECT COUNT(*) as total FROM vocabulario WHERE leccion_id BETWEEN 11 AND 23'
    );
    console.log(`\nTotal de palabras Quechua en la base de datos: ${result.rows[0].total}`);

    // Mostrar desglose por lección
    const desglose = await client.query(`
      SELECT leccion_id, COUNT(*) as cantidad 
      FROM vocabulario 
      WHERE leccion_id BETWEEN 11 AND 23 
      GROUP BY leccion_id 
      ORDER BY leccion_id
    `);
    console.log('\nDesglose por lección:');
    desglose.rows.forEach(row => {
      console.log(`  Lección ${row.leccion_id}: ${row.cantidad} palabras`);
    });

  } catch (err) {
    console.error('Error general:', err);
  } finally {
    await client.end();
    console.log('\nConexión cerrada');
  }
}

